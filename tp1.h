/* \brief suma los operandos
 * \param op1 primer número a operar.
 * \param op2 segundo número a operar.
 * \return devuelve el resultado de la suma entre los operandos.
 */

int suma(int,int);

/* \brief resta los operandos
 * \param op1 primer número a operar.
 * \param op2 segundo número a operar.
 * \return devuelve el resultado de la resta entre los operandos.
 */

int resta(int,int);

/* \brief multiplica los operandos
 * \param op1 primer número a operar.
 * \param op2 segundo número a operar.
 * \return devuelve el producto de la multiplicación entre los operandos.
 */

int multiplica(int,int);

/* \brief divide los operandos
 * \param op1 primer número a operar como dividendo.
 * \param op2 segundo número a operar como divisor.
 * \return devuelve el cociente de la división entre los operandos.
 */

int divide(int,int);

/* \brief calcula el factorial del primer operando.
 * \param op1 número a operar.
 * \return devuelve el factorial del primer operando.
 */

int factorialOp1(int);

/* \brief calcula el factorial del segundo operando.
 * \param op2 número a operar.
 * \return devuelve el factorial del segundo operando.
 */

int factorialOp2(int);


#endif // TP1_H_INCLUDED

